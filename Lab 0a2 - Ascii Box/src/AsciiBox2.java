public class AsciiBox2
{
	public static void main(String[] args)
	{
		// ANSI colors can be used to add color to terminal text
		String ANSI_CYAN = "\u001B[36m";
		String ANSI_YELLOW = "\u001B[33m";
		String ANSI_RESET = "\u001B[0m";

		System.out.println("Lucas Xie\t\t9/3/22\n");
		System.out.println("AAAAAAAAAAAAAAAAAAAAAAAAAAA");
		System.out.println("+++++++++++++++++++++++++++");
    	System.out.println("+++  " + ANSI_YELLOW + ",--," + ANSI_RESET + "\t*   \t\t+++");
		System.out.println("+++ " + ANSI_YELLOW + "/ /" + ANSI_RESET + " \t\t.  \t*   +++");
		System.out.println("+++ " + ANSI_YELLOW + "\\ {" + ANSI_RESET + "   *   \t\t.   +++");
		System.out.println("+++  " + ANSI_YELLOW + "`--`" + ANSI_RESET + "\t.  * \t\t+++");
		System.out.println("+++\t\t\t\t>o)\t\t+++");
		System.out.println("+++  (o<  \t\t//\\\t\t+++");
		System.out.println("+++  //\\  \t   (/ \\\t\t+++");
		System.out.println("+++  v_/_\t   _\\_v\t\t+++");
    	System.out.println("+++++++++++++++++++++++++++");
		System.out.println("AAAAAAAAAAAAAAAAAAAAAAAAAAA");
		System.out.println(ANSI_CYAN + "ᴄʀᴇᴅɪᴛ ᴛᴏ ᴀsᴄɪɪᴀʀᴛ.ᴇᴜ/sᴘᴀᴄᴇ/ᴍᴏᴏɴs & ᴀsᴄɪɪ.ᴄᴏ.ᴜᴋ/ᴀʀᴛ/sᴍᴀʟʟ!");
	}
}